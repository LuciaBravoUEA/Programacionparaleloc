# temperatura, ciudad, dias
# matriz 3x7x3
arreglo_temperaturas = [
    [
        [35, 45, 19, 11, 29, 36, 29],  # SEMANA 1
        [23, 41, 31, 9, 10, 14, 29],  # SEMANA 2
        [9, 25, 36, 40, 8, 36, 21],  # SEMANA 3
        [11, 25, 36, 39, 28, 9, 14],  # SEMANA 4
        [19, 28, 22, 39, 40, 19, 15],  # SEMANA 5
        [33, 45, 26, 17, 38, 21, 8],  # SEMANA 6
        [7, 9, 16, 19, 25, 35, 30],  # SEMANA 7
    ],
    [],  # Riobamba
    [],  # Manta
    [],  # Manabí
    [],  # Quito
    [],  # Loja
    [],  # Ambato
    [],  # Ibarra

]
for ciudades in range(len(arreglo_temperaturas)):
    for semana in range(len(arreglo_temperaturas[ciudades])):
        suma = 0
        promedio = 0
        for temperatura in range(len(arreglo_temperaturas[ciudades][semana])):
            suma = suma + arreglo_temperaturas[ciudades][semana][temperatura]
        promedio = suma / len(arreglo_temperaturas[ciudades][semana])
        print(f"El promedio de la semana {semana + 0} es:{promedio: 2f} para la cuidad de, Riobamba")

